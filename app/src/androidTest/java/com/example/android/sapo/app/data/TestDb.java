    public void testCreateDb() throws Throwable {
        // build a HashSet of all of the table names we wish to look for
        // Note that there will be another table in the DB that stores the
       // Android metadata (db version information)
        final HashSet<String> tableNameHashSet = new HashSet<String>();
        tableNameHashSet.add(SAPoContract.AlmacenEntry.TABLE_NAME);
        //tableNameHashSet.add(SAPoContract.CategoriaEntry.TABLE_NAME);


        mContext.deleteDatabase(SAPoDBHelper.DATABASE_NAME);
        SQLiteDatabase db = new SAPoDBHelper(
                this.mContext).getWritableDatabase();
        assertEquals(true, db.isOpen());

        // have we created the tables we want?
        Cursor c = db.rawQuery("SELECT name FROM sqlite_master WHERE type='table'", null);

        assertTrue("Error: This means that the database has not been created correctly",
                c.moveToFirst());

        // verify that the tables have been created
        do {
            tableNameHashSet.remove(c.getString(0));
        } while( c.moveToNext() );

        // if this fails, it means that your database doesn't contain both the location entry
        // and weather entry tables
        assertTrue("Error: Your database was created without both the location entry and weather entry tables",
                tableNameHashSet.isEmpty());

        // now, do our tables contain the correct columns?
        c = db.rawQuery("PRAGMA table_info(" + SAPoContract.AlmacenEntry.TABLE_NAME + ")",
                null);

        assertTrue("Error: This means that we were unable to query the database for table information.",
                c.moveToFirst());

        // Build a HashSet of all of the column names we want to look for
        final HashSet<String> almacenColumnHashSet = new HashSet<String>();
        almacenColumnHashSet.add(SAPoContract.AlmacenEntry.COLUMN_ALMACEN_ID);
        almacenColumnHashSet.add(SAPoContract.AlmacenEntry.COLUMN_ALMACEN_NOMBRE);
        almacenColumnHashSet.add(SAPoContract.AlmacenEntry.COLUMN_ALMACEN_DESCRIPCION);
        almacenColumnHashSet.add(SAPoContract.AlmacenEntry.COLUMN_ALMACEN_URL);

        int columnNameIndex = c.getColumnIndex("name");
        do {
            String columnName = c.getString(columnNameIndex);
            almacenColumnHashSet.remove(columnName);
        } while(c.moveToNext());

        // if this fails, it means that your database doesn't contain all of the required location
        // entry columns
        assertTrue("Error: The database doesn't contain all of the required location entry columns",
                almacenColumnHashSet.isEmpty());
        db.close();
    }

